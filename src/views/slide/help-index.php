<h4>Образ компании</h4>
<p>
    Слайды используются во <code>frontend</code> для привлечения внимания к продукту или услуге.
</p>
<p>
    В слайдер можно добавить фотографию, связать с ней надписи, призыв к действию, 
    ссылку (количество параметров <b>строго определенно</b> и должно быть описано в комментариях файла <code>frontend/config/params.php</code>) 
</p>
<p>
    Количество слайдов также задано в параметрах и может быть изменено.
</p>
